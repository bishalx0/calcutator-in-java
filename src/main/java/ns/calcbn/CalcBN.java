    private void getOperand(){
        
        switch(opr){
            case "+":
            case "-":
            case "*":
            case "/":
                try{
                    num2 = lblDisplay.getText().substring(num1.length() + 3);
                }
                catch(Exception e){
                    lblDisplay.setText("");
                }
                break;
                       
            case "SIN":
            case "COS":
            case "TAN":
            case "LOG":
            case "EXP":
                try{
                    num2 = lblDisplay.getText().substring(num1.length() + 7);
                }
                catch(Exception e){
                    lblDisplay.setText("");
                }
                
                break;
        }
    }
    
