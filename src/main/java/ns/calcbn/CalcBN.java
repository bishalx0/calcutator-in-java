    private void displayResult() throws Exception{
        switch(opr){
            case "+" :
                result = String.valueOf(Double.parseDouble(num1) + Double.parseDouble(num2));
                break;
            case "-":
                result = String.valueOf(Double.parseDouble(num1) - Double.parseDouble(num2));
                break;
                       
            case "*":
                result = String.valueOf(Double.parseDouble(num1) * Double.parseDouble(num2));
                break;
            case "/":
                result = String.valueOf(Double.parseDouble(num1) / Double.parseDouble(num2));
                break;
                       
            case "SIN":
                result = String.valueOf(java.lang.Math.sin(Double.parseDouble(num2)) * Double.parseDouble(num1));
                break;
                
            case "COS":
                result = String.valueOf(java.lang.Math.cos(Double.parseDouble(num2)) * Double.parseDouble(num1));
                break;
                
            case "TAN":
                result = String.valueOf(java.lang.Math.tan(Double.parseDouble(num2)) * Double.parseDouble(num1));
                break;
                
            case "LOG":
                result = String.valueOf(java.lang.Math.log(Double.parseDouble(num2)) * Double.parseDouble(num1));
                break;
                
            case "EXP":
                result = String.valueOf(java.lang.Math.exp(Double.parseDouble(num2)) * Double.parseDouble(num1));
                break;
        }
        lblDisplay.setText(result);
        num1 = result;
        opr = "";
    }
    
